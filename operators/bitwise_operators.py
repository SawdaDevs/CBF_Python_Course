a = 6 # 0110 
b = 3 # 0011

print("a:", bin(a))
print("b:", bin(b))
print("a & b:", bin(a & b))   # Bitwise AND
print("a | b:", bin(a | b))   # Bitwise OR
print("a ^ b:", bin(a ^ b))   # Bitwise XOR
print("~a:", bin(~a))         # Bitwise NOT
print("a << 1:", bin(a << 1)) # Left Shift
print("a >> 1:", bin(a >> 1)) # Right Shift
